public class modificandoArrays {
    public static void main(String[] args) {
        int[] arrayExemplo = new int[]{1, 2, 3, 4, 5}; // Outra forma de inicializar um array.
        System.out.println("O valor atual do indice 0 é " + arrayExemplo[0]);
        //Modificando o valor atual do indice 0
        arrayExemplo[0]=33;
        System.out.println("O Atual valor do indice 0 é " + arrayExemplo[0]);
       } 
}
