public class condicionalElseIf {
   public static void main (String[] args){
    int nota=60;
    int media=65;
    if(nota>=media){
        System.out.println("O aluno foi aprovado");
    }else{
         System.out.println("Aluno está em recuperação");
    }
    System.out.print("Fim do programa");
   } 
}
